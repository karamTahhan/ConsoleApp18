using System;

namespace For_loppar
{
    class program
    {
       static void Main(string[] args)
        {
            for (int i=10; i<=30;  i++)
            {
                Console.WriteLine(i);
            }
            for (int x=200; x>=180;  x--)
            {
                Console.Write(x);
            }
            for (int y=1000;  y<=1400; y+=50)
            {
                Console.WriteLine(y);
            }

        }



    }




}